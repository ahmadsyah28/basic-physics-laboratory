<script>
document.addEventListener('DOMContentLoaded', function() {
    const equipmentElements = document.querySelectorAll('.equipment-animate');
    const equipmentSection = document.getElementById('equipment');
    let lastScrollY = window.scrollY;
    let equipmentSectionTop = equipmentSection.offsetTop - 200; // Mulai animasi lebih awal

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            const currentScrollY = window.scrollY;
            const scrollDirection = currentScrollY > lastScrollY ? 'down' : 'up';

            if (entry.isIntersecting) {
                // Animasi dengan delay berdasarkan data-delay
                const delay = entry.target.dataset.delay || 0;
                setTimeout(() => {
                    entry.target.classList.add('animate');
                }, delay);
            } else {
                // Reset animasi hanya jika scroll ke atas dan berada di atas section
                if (scrollDirection === 'up' && currentScrollY < equipmentSectionTop) {
                    entry.target.classList.remove('animate');
                }
            }

            lastScrollY = currentScrollY;
        });
    }, {
        threshold: 0.1,
        rootMargin: '0px 0px -100px 0px'
    });

    equipmentElements.forEach(element => {
        observer.observe(element);
    });

    // Update posisi section saat window resize
    window.addEventListener('resize', () => {
        equipmentSectionTop = equipmentSection.offsetTop - 200;
    });
});
</script>
